function detectLanguage() {
  const stored = localStorage.getItem('dilink-lang');
  if (stored && translations[stored]) return stored;
  const browser = (navigator.language || 'en').split('-')[0];
  const full = navigator.language || 'en';
  if (translations[full]) return full;
  const match = Object.keys(translations).find(k => k.startsWith(browser));
  return match || 'en';
}

let currentLang = detectLanguage();

function applyLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('dilink-lang', lang);
  const t = translations[lang];
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) el.textContent = t[key];
  });
  const sel = document.getElementById('langSelect');
  if (sel) sel.value = lang;
}

document.addEventListener('DOMContentLoaded', () => {
  applyLanguage(currentLang);
  const sel = document.getElementById('langSelect');
  if (sel) sel.addEventListener('change', e => applyLanguage(e.target.value));
});
